package day10.TriAngle;

public class TirAngle {
    private int  base;
    private int height;
    TirAngle (){
        System.out.println("gzq");
    }
    TirAngle(int Base,int Height){
        this();
        base = Base;
        height = Height;
    }
    public void setBase(int base) {
        this.base = base;
    }
    public int getBase() {
        return base;
    }
    public void setHeight(int height) {
        this.height = height;
    }
    public int getHeight() {
        return height;
    }
}